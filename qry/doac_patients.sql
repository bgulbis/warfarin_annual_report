SELECT DISTINCT
	ENCOUNTER.ENCNTR_ID AS ENCOUNTER_ID
FROM
	CLINICAL_EVENT,
	ENCOUNTER
WHERE
	CLINICAL_EVENT.EVENT_CD IN (
		894197564, -- apixaban
		535736194, -- dabigatran
		1466817862, -- edoxaban
		642177890 -- rivaroxaban
	)
	AND CLINICAL_EVENT.EVENT_END_DT_TM BETWEEN 
		pi_to_gmt(DATE '2016-07-01', pi_time_zone(2, @Variable('BOUSER'))) 
		AND pi_to_gmt(DATE '2019-07-01', pi_time_zone(2, @Variable('BOUSER')))
	AND CLINICAL_EVENT.VALID_UNTIL_DT_TM > DATE '2099-12-31'
	AND (
		CLINICAL_EVENT.ENCNTR_ID = ENCOUNTER.ENCNTR_ID
		AND ENCOUNTER.ACTIVE_IND = 1
		AND ENCOUNTER.LOC_FACILITY_CD IN (
			3310, -- HH HERMANN
			3796, -- HC Childrens
			3821, -- HH Clinics
			3822, -- HH Trans Care
			3823 -- HH Rehab
		)
	)
